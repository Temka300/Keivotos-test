// build a browse hash; omit q from the URL while in private mode
function browseHref(q, page, sort) {
  const tail = `page=${page}&sort=${encodeURIComponent(sort)}`;
  return privateMode ? `#/browse?${tail}` : `#/browse?q=${encodeURIComponent(q)}&${tail}`;
}

